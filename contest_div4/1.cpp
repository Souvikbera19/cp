#include <bits/stdc++.h>
using namespace std;
using ll = long long;

int main() {


    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while(t--){
        ll n;
        cin >> n;
        for(int i =1;i<=n;i++){
            cout << i << ' ';
        }
        cout << '\n';
    }

    return 0;
}